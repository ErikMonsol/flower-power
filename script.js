function renderProducts(productsData) {
    const productsDiv = document.getElementById('products');

    let rowDiv; // To store the current row

    productsData.forEach((product, index) => {
        // Create a new div for the product
        const productDiv = document.createElement('div');
        productDiv.classList.add('product');

        // Add product name
        const productName = document.createElement('h2');
        productName.textContent = product.name;
        productDiv.appendChild(productName);

        // Add product image
        if (product.images && product.images.length > 0) {
            const productImage = document.createElement('img');
            productImage.src = product.images[0].src;
            productImage.alt = product.images[0].alt;
            productDiv.appendChild(productImage);
        }

        // Add product description
        const productDesc = document.createElement('p');
        productDesc.innerHTML = product.description;
        productDiv.appendChild(productDesc);

        // Add product price
        const productPrice = document.createElement('p');
        productPrice.innerHTML = product.price_html;
        productDiv.appendChild(productPrice);

        // Append the product div to the current row
        if (index % 3 === 0) {
            rowDiv = document.createElement('div');
            rowDiv.classList.add('row');
            productsDiv.appendChild(rowDiv);
        }
        rowDiv.appendChild(productDiv);
    });
}

// Render products when the page loads
